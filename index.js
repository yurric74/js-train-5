// Завдання 2: Дано масив, використовуючи цикл "for in" знайдіть суму всіх елементів масиву
function sumArray(array) {
  let mass=0;// Ініціалізуємо змінну для зберігання суми
  for (const num in array) {// Проходимося по всіх індексах масиву
  mass+=array[num];}// Додаємо кожен елемент до загальної суми
  return mass;// Повертаємо суму
}

