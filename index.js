// Завдання 10: Створити функцію, яка приймає масив чисел як аргумент.
function swapMinMax(array) {
  let max=array[0]; let min=array[0];// Ініціалізуємо дві змінні (max і min) з першим елементом масиву. Ці змінні будуть використовуватися для зберігання максимального та мінімального значень в масиві.
  let maxIndex=0;     let minIndex=0;// Ініціалізуємо дві змінні (maxIndex і minIndex) з нульового значення. Ці змінні будуть використовуватися для зберігання індексів максимального та мінімального значень в масиві.
  for (let i=0; i < array.length; i++) {// Використовуємо цикл for для ітерації по масиву. Починаємо з першого елемента (i = 0) і продовжуємо до останнього елемента масиву (i < array.length).
  if (array[i]>max) {max=array[i]; maxIndex=i;}// Умова if перевіряє, чи є поточний елемент масиву більшим за поточне максимальне значення. Якщо це так, оновлюємо max з поточним елементом масиву та maxIndex з поточним індексом.
  else if (array[i]<min) {// Умова else if перевіряє, чи є поточний елемент масиву меншим за поточне мінімальне значення. Якщо це так, оновлюємо min з поточним елементом масиву та minIndex з поточним індексом.
  min=array[i]; minIndex=i;}}// Застосовуємо деструктуризацію масиву для обміну місцями мінімального та максимального значення в масиві.
  [array[minIndex],array[maxIndex]]=[array[maxIndex],array[minIndex]];
  return array;// Функція повертає масив, в якому мінімальне та максимальне значення обміняні місцями.
}

