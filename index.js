function findMinMax([firstElement, ...restElements]) {
  let minValue = firstElement; let maxValue = firstElement;// Ініціалізуємо змінні для мінімального та максимального значень, задаємо їм перше значення масиву
  for (const element of restElements) {// Перебираємо масив починаючи з другого елементу
  if (element < minValue) {// Перевіряємо, чи поточний елемент менший за мінімальне значення
  minValue = element;}// Якщо так, оновлюємо мінімальне значення
  if (element > maxValue) {// Перевіряємо, чи поточний елемент більший за максимальне значення
    maxValue = element;}}// Якщо так, оновлюємо максимальне значення
  console.log(`Min value:${minValue}`);// Виводимо мінімальне та максимальне значення
  console.log(`Max value:${maxValue}`);
}

console.log("Завдання 6 ====================================");
findMinMax([5, 2, 8, 1, 9, 3]); // Виведе Min: 1 Max: 9

